package com.procter.model;

public class TimeStamp {

    public String min;
    public String sec;

    public String getMin() {
        return min;
    }

    public String getSec() {
        return sec;
    }

    public void setMin(String min) {
        this.min = min;
    }

    public void setSec(String sec) {
        this.sec = sec;
    }
}
